#include "3-calc.h"

/**
 * op_add - a function that returns sum
 * @a: first integer
 * @b: second integer
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that returns difference
 * @a: first integer
 * @b: second integer
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that returns product
 * @a: first integer
 * @b: second integer
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that returns result of division
 * @a: first integer
 * @b: second integer
 *
 * Return: result of division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that returns remainder of division
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
